package bma.siteone.clound;

public class CloundException extends RuntimeException {

	public CloundException() {
		super();
	}

	public CloundException(String message, Throwable cause) {
		super(message, cause);
	}

	public CloundException(String message) {
		super(message);
	}

	public CloundException(Throwable cause) {
		super(cause);
	}

}
